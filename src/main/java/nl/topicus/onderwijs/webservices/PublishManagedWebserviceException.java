package nl.topicus.onderwijs.webservices;

public class PublishManagedWebserviceException extends Exception
{

	private static final long serialVersionUID = 1L;

	public PublishManagedWebserviceException(String message)
	{
		super(message);
	}

	public PublishManagedWebserviceException(Throwable cause)
	{
		super(cause);
	}

	public PublishManagedWebserviceException(String message, Throwable cause)
	{
		super(message, cause);
	}

}
